package com.ouyang.entry;

public class Person
{
  private Integer id;
  private String name;
  private Short age;
  public Integer getId()
  {
    return this.id;
  }
  public void setId(Integer id)
  {
    this.id = id;
  }
  public String getName()
  {
    return this.name;
  }
  public void setName(String name)
  {
    this.name = name;
  }
  public Short getAge()
  {
    return this.age;
  }
  public void setAge(Short age)
  {
    this.age = age;
  }
  
  
}
